#!/bin/bash

# ============================================================
# СКРИПТ РАЗВЕРТЫВАНИЯ Django ПРИЛОЖЕНИЯ
# Микросервис обработки файлов JSON/CSV
# Автор: Кутилов Б.А.
# ============================================================

# Очистка экрана для наглядности
clear

echo "╔════════════════════════════════════════════════════════════╗"
echo "║     МИКРОСЕРВИС ОБРАБОТКИ ФАЙЛОВ JSON/CSV                 ║"
echo "║     РАЗВЕРТЫВАНИЕ ПРИЛОЖЕНИЯ                              ║"
echo "╚════════════════════════════════════════════════════════════╝"
echo ""

# 1. Проверка наличия Python
echo "📌 [1/6] Проверка Python..."
if command -v python &> /dev/null; then
    echo "✅ Python найден: $(python --version)"
else
    echo "❌ Ошибка: Python не установлен"
    exit 1
fi
echo ""

# 2. Установка зависимостей
echo "📌 [2/6] Установка зависимостей..."
echo "   - Django (веб-фреймворк)"
echo "   - Pandas (обработка данных)"
echo "   - Matplotlib (визуализация)"
echo "   - NumPy (математические вычисления)"
pip install django pandas matplotlib numpy
echo "✅ Зависимости установлены"
echo ""

# 3. Проверка установки
echo "📌 [3/6] Проверка установленных пакетов..."
echo "   Установленные версии:"
pip list | grep -E "django|pandas|matplotlib|numpy" | sed 's/^/   /'
echo ""

# 4. Создание необходимых директорий
echo "📌 [4/6] Создание директорий..."
mkdir -p uploads
mkdir -p media
echo "✅ Директории созданы: uploads/, media/"
echo ""

# 5. Выполнение миграций Django
echo "📌 [5/6] Выполнение миграций базы данных..."
python manage.py migrate
echo "✅ Миграции выполнены"
echo ""

# 6. Запуск сервера
echo "📌 [6/6] Запуск сервера..."
echo ""
echo "╔════════════════════════════════════════════════════════════╗"
echo "║ ✅ РАЗВЕРТЫВАНИЕ УСПЕШНО ЗАВЕРШЕНО!                        ║"
echo "║                                                            ║"
echo "║ 🌐 Сервер запущен по адресу: http://0.0.0.0:8000          ║"
echo "║ 📱 Откройте Webview или перейдите по ссылке               ║"
echo "║                                                            ║"
echo "║ 💡 Для остановки сервера нажмите Ctrl+C                   ║"
echo "╚════════════════════════════════════════════════════════════╝"
echo ""

# Запуск сервера
python manage.py runserver 0.0.0.0:8000